# Function to calculate the area of a square



# Taking input from the user
length = float(input("Enter the length of a side of the square in cm: "))

# Calculating area and perimeter
area=length ** 2
perimeter= 4 * length

# Displaying the results
print("Entered length of a side of the square: ",length, "cm")
print("The area of the square is : ",area, "suare cm")
print("The perimeter of the square is : ",perimeter,"cm")
